document.querySelector(".menu span").onclick = function(){
  document.querySelector(".menu").style.left = "-100%";
  document.querySelector(".fermerMenu").style.display = "none";
}

document.querySelector(".fermerMenu").onclick = function(){
  document.querySelector(".menu").style.left = "-100%";
  document.querySelector(".fermerMenu").style.display = "none";
}

document.querySelectorAll(".menu li").forEach((item, i) => {
  item.onclick = function(){
    document.querySelector(".menu").style.left = "-100%";
    document.querySelector(".fermerMenu").style.display = "none";
  }
});

document.querySelectorAll(".subnav h3").forEach((item, i) => {
  item.onclick = function(){
    if(document.querySelectorAll(".subnav-content")[i].style.display == "" || document.querySelectorAll(".subnav-content")[i].style.display == "none"){
      document.querySelectorAll(".subnav-content")[i].style.display = "flex";
    }
    else {
      document.querySelectorAll(".subnav-content")[i].style.display = "none";
    }
  }
});
