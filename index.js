if(document.querySelector(".container")){
  document.querySelector(".container").addEventListener("click", function(e){
    if(e.target.classList.contains("info") == false){
      document.querySelector(".container").style.display = "none";
      document.querySelector(".info").innerHTML = "";
    }
  });
}

document.querySelector(".menu").style.left = "-100%";

function afficheMenu(){
  if(document.querySelector(".menu").style.left == "-100%"){
    document.querySelector(".menu").style.left = 0;
    document.querySelector(".fermerMenu").style.display = "block";
  }
  else {
    document.querySelector(".menu").style.left = "-100%";
    document.querySelector(".fermerMenu").style.display = "none";
  }
}

